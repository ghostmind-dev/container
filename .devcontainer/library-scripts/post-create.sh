#!/usr/bin/env bash

############################################################################
# SET DOCKER WITH GHCR
############################################################################

echo $GITHUB_TOKEN | docker login ghcr.io -u USERNAME --password-stdin

# ############################################################################
# # ZSH SETUP
# ############################################################################

sed -i '/plugins=(git)/c\plugins=(git kubectl zsh-autosuggestions gcloud docker)' ~/.zshrc

############################################################################
# ALIASES
############################################################################

cat <<EOT >>~/.zshrc
alias home="cd ${SRC}"
EOT

############################################################################
# BIN
############################################################################

cat <<EOT >>~/.zshrc
export PATH=\$PATH:/usr/local/go/bin 
EOT
